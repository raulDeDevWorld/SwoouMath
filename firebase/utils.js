function handleSignOut() {
      auth.signOut().then(function() {
            // Sign-out successful.
      }).catch(function(error) {
            // An error happened.
      });     
}


const db = firebase.database()

export { auth, onAuth, withFacebook, withGoogle, handleSignOut }